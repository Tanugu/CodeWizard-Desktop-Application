    private void jEmpIdcomboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEmpIdcomboActionPerformed
     try{
         String empid=jEmpIdcombo.getSelectedItem().toString();
         Employeepojo e= EmployeeDao.findEmpById(empid);
         Empnametxt.setText(e.getEmpname());
         salarytxt.setText(String.valueOf(e.getSalary()));
         jobcombo.setSelectedItem(e.getJob());
     }
      catch(Exception ex)
      {
            System.out.println("error in combobox action performed");
            JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jEmpIdcomboActionPerformed

