    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null,"Please input all the fields!","Error!",JOptionPane.ERROR_MESSAGE);
      return;
        }
        try{
        Employeepojo e= new Employeepojo();
        e.setEmpid(jEmpIdcombo.getSelectedItem().toString());
        e.setEmpname(Empnametxt.getText());
        e.setJob(jobcombo.getSelectedItem().toString());
        e.setSalary(Integer.parseInt(salarytxt.getText()));
        //boolean result=EmployeeDao.updateEmployee(e);
        boolean result = EmployeeDao.updateEmployee(e);

        if(result){
             
            JOptionPane.showMessageDialog(null,"recoprd updated successfully","updation done",JOptionPane.INFORMATION_MESSAGE);
            
        }
        else{
          
            JOptionPane.showMessageDialog(null,"updation not done","Error!",JOptionPane.ERROR_MESSAGE);
          
        }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }         
        }

